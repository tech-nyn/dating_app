
class User{
  final int id;
  final int age;
  final String name;
  final String picUrl;
  final bool isOnline;
  final String gender;
  final String location;
  final String interestedIn;

  User({
    required this.id,
    required this.age,
    required this.name,
    required this.picUrl,
    required this.isOnline,
    required this.gender,
    required this.location,
    required this.interestedIn
  });
}